const SORTED_KANA_GLOSSARY = [...KANA_GLOSSARY].sort(
  (left, right) => right[0].length - left[0].length,
);

const escapeRegExp = (value: string): string =>
  value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

export const withKana = (text: string): string => {
  if (!SORTED_KANA_GLOSSARY.some(([term]) => text.includes(term))) {
    return text;
  }

  return SORTED_KANA_GLOSSARY.reduce((currentText, [term, annotated]) => {
    const pattern = new RegExp(`${escapeRegExp(term)}(?![（(])`, 'g');
    return currentText.replace(pattern, annotated);
  }, text);
};

